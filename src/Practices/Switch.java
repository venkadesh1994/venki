package Practices;

public class Switch {
public static void main(String[] args) {
	int a=8;
	switch (a) {
	case 1:
		System.out.println("sunday");
		break;
	case 2:
		System.out.println("tuesday");
		break;
	case 3:
		System.out.println("wednesdy");
		break;
	case 4:
		System.out.println("thursday");
		break;
	default:
		System.out.println("invalid");
		break;
	}
}
}
